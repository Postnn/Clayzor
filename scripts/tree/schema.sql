-- Демо-таблица для ClayTreeView: одна таблица — две модели иерархии.
-- NestedSet: ЛевыйКлюч/ПравыйКлюч/Уровень
-- ParentKey: КодРодителя (ссылка на родителя)

IF OBJECT_ID('dbo.ClayTreeDemo', 'U') IS NULL
BEGIN
    CREATE TABLE dbo.ClayTreeDemo (
        КодУзла      int           NOT NULL PRIMARY KEY,
        Название     nvarchar(200) NOT NULL,
        КодРодителя  int           NULL,
        ЛевыйКлюч    int           NOT NULL,
        ПравыйКлюч   int           NOT NULL,
        Уровень      int           NOT NULL
    );

    -- 14 узлов, 2 корня, 3 уровня
    -- Арифметика NestedSet: R = L + 2×(число_потомков_всех_уровней) + 1
    INSERT INTO dbo.ClayTreeDemo (КодУзла, Название, КодРодителя, ЛевыйКлюч, ПравыйКлюч, Уровень) VALUES
    ( 1, N'Оборудование',              NULL,  1, 24, 0),
    ( 2, N'Компьютерная техника',        1,  2, 13, 1),
    ( 3, N'Ноутбуки',                    2,  3,  8, 2),
    ( 4, N'Ноутбук Dell Latitude',       3,  4,  5, 3),
    ( 5, N'Ноутбук HP ProBook',          3,  6,  7, 3),
    ( 6, N'Мониторы',                    2,  9, 12, 2),
    ( 7, N'Монитор Dell 24"',            6, 10, 11, 3),
    ( 8, N'Мебель',                      1, 14, 21, 1),
    ( 9, N'Столы',                       8, 15, 18, 2),
    (10, N'Стол письменный',             9, 16, 17, 3),
    (11, N'Стулья',                      8, 19, 20, 2),
    (12, N'Прочее',                      1, 22, 23, 1),
    (13, N'Программное обеспечение',  NULL, 25, 28, 0),
    (14, N'Лицензии',                   13, 26, 27, 1);

    -- Контрольная проверка: ПравыйКлюч - ЛевыйКлюч должно быть нечётным (2×n + 1)
    -- и соответствовать числу потомков
    DECLARE @bad int;
    SELECT @bad = COUNT(*)
    FROM dbo.ClayTreeDemo t
    WHERE (t.ПравыйКлюч - t.ЛевыйКлюч) % 2 <> 1
       OR (t.ПравыйКлюч - t.ЛевыйКлюч - 1) / 2 <>
          (SELECT COUNT(*) FROM dbo.ClayTreeDemo c
           WHERE c.ЛевыйКлюч > t.ЛевыйКлюч AND c.ПравыйКлюч < t.ПравыйКлюч);
    IF @bad > 0
        THROW 50000, 'Ошибка арифметики NestedSet: расхождение ключей.', 1;

    PRINT 'ClayTreeDemo: таблица создана, сид-данные вставлены.';
END
ELSE
    PRINT 'ClayTreeDemo: таблица уже существует.';
